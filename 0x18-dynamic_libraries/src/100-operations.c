
/**
 * add - Add 2 numbers
 * @a: The first number
 * @b: The second number
 *
 * Return: The result
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - Subtract 2 numbers
 * @a: The first number
 * @b: The second number
 *
 * Return: The result
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - Multiply 2 numbers
 * @a: The first number
 * @b: The second number
 *
 * Return: The result
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - Divide 2 numbers
 * @a: The first number
 * @b: The second number
 *
 * Return: The result
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - Calculate the modules of the division between 2 numbers
 * @a: The first number
 * @b: The second number
 *
 * Return: The result
 */
int mod(int a, int b)
{
	return (a % b);
}
